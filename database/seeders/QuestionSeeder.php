    private function seedSpeedResilience(): void
    {
        $symbols = ['○', '△', '□', '◇', '☆', '◎', '▽', '■', '◆', '★'];
        $pool = [];

        for ($i = 0; $i < 24; $i++) {
            $targetIdx = $i % count($symbols);
            $targetSymbol = $symbols[$targetIdx];
            $hasTarget = ($i % 2 === 0);

            $otherSymbols = array_values(array_diff($symbols, [$targetSymbol]));
            shuffle($otherSymbols);

            if ($hasTarget) {
                $rightGroup = [$targetSymbol];
                $rightGroup = array_merge($rightGroup, array_slice($otherSymbols, 0, 4));
                shuffle($rightGroup);
            } else {
                $rightGroup = array_slice($otherSymbols, 0, 5);
            }

            $groupStr = implode(' ', $rightGroup);
            $pool[] = [
                'target'  => $targetSymbol,
                'group'   => $groupStr,
                'correct' => $hasTarget ? '○' : '×',
            ];
        }

