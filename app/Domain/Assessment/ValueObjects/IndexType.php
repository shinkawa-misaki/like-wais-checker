<?php

declare(strict_types=1);

namespace App\Domain\Assessment\ValueObjects;

/**
 * 認知の手すりチェック Lite — 4資源指数
 */
enum IndexType: string
{
    case VCI = 'VCI'; // 言語整理
    case PRI = 'PRI'; // 構造理解
    case WMI = 'WMI'; // 保持操作
    case PSI = 'PSI'; // 速度耐性

    public function label(): string
    {
        return match ($this) {
            self::VCI => '言語整理',
            self::PRI => '構造理解',
            self::WMI => '保持操作',
            self::PSI => '速度耐性',
        };
    }

    public function maxScore(): int
    {
        return match ($this) {
            self::VCI => 12, // A: 6問 × 2点
            self::PRI => 6,  // B: 6問 × 1点
            self::WMI => 6,  // C: 6問 × 1点
            self::PSI => 6,  // D: 6問 × 1点
        };
    }

    /**
     * percentageLevel() が返す5段階の水準に応じた解釈テキストを返す。
     */
