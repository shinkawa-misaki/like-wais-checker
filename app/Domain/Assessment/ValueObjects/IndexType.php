<?php

declare(strict_types=1);

namespace App\Domain\Assessment\ValueObjects;

enum IndexType: string
{
    case VCI = 'VCI'; // 言語理解
    case PRI = 'PRI'; // 知覚推理
    case WMI = 'WMI'; // ワーキングメモリー
    case PSI = 'PSI'; // 処理速度

    public function label(): string
    {
        return match ($this) {
            self::VCI => '言語理解 (VCI)',
            self::PRI => '知覚推理 (PRI)',
            self::WMI => 'ワーキングメモリー (WMI)',
            self::PSI => '処理速度 (PSI)',
        };
    }

    public function maxScore(): int
    {
        return match ($this) {
            self::VCI => 40, // A(20) + B(20)
            self::PRI => 22, // C(12) + D(10)
            self::WMI => 28, // E(18) + F(10)
            self::PSI => 84, // G(24) + H(60)
        };
    }

    /**
     * percentageLevel() が返す5段階の水準に応じた解釈テキストを返す。
     *
     * $level: 'かなり強い' | '強め' | '平均域' | '弱め' | 'かなり弱い'
     */
    public function interpretationByLevel(string $level): string
    {
        return match ($this) {
            self::VCI => match ($level) {
                'かなり強い' =>
                    '言語での整理・抽象化・説明が突出しています。複雑な概念を誰もが理解できる言葉に変換する力があり、'
                    . '書く・話す・命名すること自体が思考の加速になります。'
                    . 'ドキュメント作成・企画立案・説明役として大きな強みを発揮できます。',
                '強め' =>
                    '言語化で思考を整理する力があります。説明・要約・手順書化が得意で、'
                    . '言葉で問題の輪郭を作ることができます。'
                    . 'ミーティングのまとめ役や文書作成で特に力を発揮しやすいです。',
                '平均域' =>
                    '言語整理は標準的です。言葉にする作業を丁寧に行うことで安定したアウトプットが出せます。'
                    . '図解や箇条書きを補助ツールとして使うとさらにスムーズです。',
                '弱め' =>
                    '言語化にエネルギーがかかりやすい状態です。いきなり文章化せず、'
                    . '図解→短文→箇条書きの順で外在化すると負荷が減ります。'
                    . '「最初から綺麗に言わない」と決めると心理的なハードルが下がります。',
                default => // かなり弱い
                    '言語化の負荷が高い状態です。音声メモや図・絵から始め、後でテキスト化するプロセスが有効です。'
                    . 'テンプレートや定型文を積極的に活用し、ゼロから書く機会を減らす環境設計が助けになります。',
            },

            self::PRI => match ($level) {
                'かなり強い' =>
                    '規則・型・構造を即座に把握する力が突出しています。'
                    . '複雑な情報を分類・フレーム化し、先に枠組みを置いてから詰める思考が得意です。'
                    . 'アーキテクチャ設計・企画の骨格作り・ロジック構築で頭角を現せます。',
                '強め' =>
                    'パターンの発見と構造の把握が得意です。「型を先に置く」アプローチで効率よく作業できます。'
                    . '分類・テンプレート化・整理整頓が武器になります。',
                '平均域' =>
                    '構造理解は標準的です。手順を明文化し、例を増やして確認するアプローチが安定感をもたらします。'
                    . '一軸ずつ丁寧に検証していくことで確実に進められます。',
                '弱め' =>
                    '情報が散らかりやすく、規則が見える前に消耗しやすい状態です。'
                    . '条件を一つずつ確認し、例を増やして類推する戦略が有効です。'
                    . '一度に処理する情報量を意識的に減らすと改善しやすくなります。',
                default => // かなり弱い
                    '規則や構造の把握に高い負荷がかかっています。'
                    . '情報をフロー図やマインドマップで視覚化してから考える手順が助けになります。'
                    . '最初は「真似る・型を借りる」ことから入り、パターンを体で覚えていく方法が向いています。',
            },

            self::WMI => match ($level) {
                'かなり強い' =>
                    '複数の情報を頭に保持しながら操作する力が突出しています。'
                    . '並行処理・マルチタスク・複数条件を抱えた作業でも崩れにくく、'
                    . '複雑な会議や交渉の場でも着いていける強みがあります。',
                '強め' =>
                    '情報を保持しながら処理できる力があります。複数の条件を同時に扱う場面でも安定しています。'
                    . '過負荷のサインを早めに察知し、適切に休むことでパフォーマンスを維持できます。',
                '平均域' =>
                    'ワーキングメモリーは標準的です。メモや外部ツールをうまく活用することで'
                    . '安定したパフォーマンスを維持できます。作業の途中で内容を書き出す習慣が助けになります。',
                '弱め' =>
                    '保持容量が先に埋まり、処理速度が落ちやすい状態です。'
                    . '途中メモ・チェックリスト・「1タスク＝1画面」の分割が効果的です。'
                    . 'タスクをとにかく小さくして一つずつ消化する方が安定します。',
                default => // かなり弱い
                    'ワーキングメモリーへの負荷が高い状態です。'
                    . '外部記憶（メモ・ノート・ツール）を積極的に使い、「覚えない」ことを前提にした仕組みが重要です。'
                    . '一度に一つのことだけに集中できる環境を意識して整えましょう。',
            },

            self::PSI => match ($level) {
                'かなり強い' =>
                    '時間圧の中での処理速度・切替速度が突出しています。'
                    . 'ルーチン処理・短時間集中・素早い判断が求められる場面で大きな強みを発揮します。'
                    . '締切が厳しい局面でも焦らず実力を出せます。',
                '強め' =>
                    '時間圧に対する耐性があります。定型作業やタイムプレッシャーのある場面でも崩れにくく、'
                    . 'テンポよく作業を進められます。速度が求められる場面で頼りにされやすいです。',
                '平均域' =>
                    '処理速度は標準的です。焦らず着実に進めることで安定したアウトプットが出せます。'
                    . '時間を区切って集中するポモドーロ式の作業が向いています。',
                '弱め' =>
                    '焦ると崩れやすく、慎重さが勝つ傾向があります。'
                    . '速度勝負の役割より正確さ優先の役割が向いています。'
                    . '締切は前倒しと小分けで設定し、余裕を作る設計が有効です。',
                default => // かなり弱い
                    '時間圧への耐性に高い負荷がかかっています。'
                    . 'タイムプレッシャーのある環境を可能な限り避け、自分のペースで進める仕事設計が重要です。'
                    . 'バッファを多めに確保した締切設定と、速度より品質を評価される役割配置を検討しましょう。',
            },
        };
    }

    /**
     * @deprecated interpretationByLevel() を使用してください
     */
    public function strengthDescription(): string
    {
        return $this->interpretationByLevel('強め');
    }

    /**
     * @deprecated interpretationByLevel() を使用してください
     */
    public function weaknessDescription(): string
    {
        return $this->interpretationByLevel('弱め');
    }
}
