            self::VCI => match ($level) {
                'かなり強い' =>
                    '要点の抽出・言い換え・優先順位づけが非常に得意です。'
                    . '複雑な情報を分かりやすい言葉に変換する力があり、'
                    . 'ドキュメント作成・企画立案・説明役として大きな強みを発揮できます。',
                '強め' =>
                    '言語化で思考を整理する力があります。要約や手順書化が得意で、'
                    . '言葉で問題の輪郭を作ることができます。',
                '平均域' =>
                    '言語整理は標準的です。箇条書きや図解を補助ツールとして使うとさらにスムーズです。',
                '弱め' =>
                    '言語化にエネルギーがかかりやすい状態です。'
                    . 'いきなり文章化せず、図解→短文→箇条書きの順で外在化すると負荷が減ります。',
                default =>
                    '言語化の負荷が高い状態です。音声メモや図・絵から始め、後でテキスト化するプロセスが有効です。'
                    . 'テンプレートや定型文を積極的に活用しましょう。',
            },

            self::PRI => match ($level) {
                'かなり強い' =>
                    '規則・型・構造を即座に把握する力が突出しています。'
                    . '先に枠組みを置いてから詰める思考が得意で、設計・企画の骨格作りで頭角を現せます。',
                '強め' =>
                    'パターンの発見と構造の把握が得意です。「型を先に置く」アプローチで効率よく作業できます。',
                '平均域' =>
                    '構造理解は標準的です。手順を明文化し、例を増やして確認するアプローチが安定感をもたらします。',
                '弱め' =>
                    '情報が散らかりやすく、規則が見える前に消耗しやすい状態です。'
                    . '条件を一つずつ確認し、例を増やして類推する戦略が有効です。',
                default =>
                    '規則や構造の把握に高い負荷がかかっています。'
                    . 'フロー図やマインドマップで視覚化してから考える手順が助けになります。',
            },

            self::WMI => match ($level) {
                'かなり強い' =>
                    '複数の情報を頭に保持しながら操作する力が突出しています。'
                    . '並行処理や複数条件を抱えた作業でも崩れにくい強みがあります。',
                '強め' =>
                    '情報を保持しながら処理できる力があります。複数の条件を同時に扱う場面でも安定しています。',
                '平均域' =>
                    '保持操作は標準的です。メモや外部ツールをうまく活用することで安定したパフォーマンスを維持できます。',
                '弱め' =>
                    '保持容量が先に埋まり、処理速度が落ちやすい状態です。'
                    . '途中メモ・チェックリスト・「1タスク＝1画面」の分割が効果的です。',
                default =>
                    '保持操作への負荷が高い状態です。'
                    . '外部記憶を積極的に使い、「覚えない」ことを前提にした仕組みが重要です。',
            },

            self::PSI => match ($level) {
                'かなり強い' =>
                    '時間圧の中での処理速度・切替速度が突出しています。'
                    . '焦りに崩れにくく、締切が厳しい局面でも実力を出せます。',
